const VectorFont = {
  glyphs,
  getTextWidth(text, size) {
    return text.length * size;
  },
  drawText(graphics, text, x, y, size, color, strokeWidth) {
    const scale = size / 6;
    let currentX = x;
    if (strokeWidth !== undefined && color !== undefined) {
      graphics.lineStyle(strokeWidth, color);
    }
    for (let i = 0; i < text.length; i++) {
      const char = text[i].toUpperCase();
      const glyph = glyphs[char] || glyphs[' '];
      if (glyph) {
        for (const line of glyph) {
          const x1 = currentX + line[0] * scale;
          const y1 = y + line[1] * scale;
          const x2 = currentX + line[2] * scale;
          const y2 = y + line[3] * scale;
          graphics.lineBetween(x1, y1, x2, y2);
        }
      }
      currentX += 6 * scale;
    }
  }
};

